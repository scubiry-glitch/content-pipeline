// ============================================================
// parse / render YAML frontmatter
// ============================================================

const FRONT_RE = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?/;

export function parseFrontmatter(md: string): ParseResult {
  const m = md.match(FRONT_RE);
  if (!m) return { frontmatter: {}, body: md };

  const raw = m[1];
  const body = md.slice(m[0].length);
  const frontmatter = parseYaml(raw);
  return { frontmatter, body };
}

/** 微型 YAML parser — 只支持本模块写出的形态（标量 / 数组 / JSON-stringified 对象数组） */
function parseYaml(raw: string): WikiFrontmatter {
  const out: Record<string, unknown> = {};
  const lines = raw.split(/\r?\n/);
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    if (!line.trim() || line.trimStart().startsWith('#')) { i += 1; continue; }
    const m = line.match(/^([A-Za-z_][A-Za-z0-9_]*)\s*:\s*(.*)$/);
    if (!m) { i += 1; continue; }
    const key = m[1];
    const rest = m[2];

    if (rest === '') {
      // 数组形态：连续 `  - item`
      const arr: unknown[] = [];
      i += 1;
      while (i < lines.length && /^\s+-\s+/.test(lines[i])) {
        arr.push(parseScalar(lines[i].replace(/^\s+-\s+/, '').trim()));
        i += 1;
      }
      out[key] = arr;
    } else {
      out[key] = parseScalar(rest);
      i += 1;
    }
  }
  return out as WikiFrontmatter;
}

function parseScalar(s: string): unknown {
  const t = s.trim();
  if (!t) return '';
  // 引号字符串
  if ((t.startsWith('"') && t.endsWith('"')) || (t.startsWith("'") && t.endsWith("'"))) {
    return t.slice(1, -1).replace(/\\"/g, '"').replace(/\\'/g, "'");
  }
  // 行内数组 [a, b]
  if (t.startsWith('[') && t.endsWith(']')) {
    const inner = t.slice(1, -1).trim();
    if (!inner) return [];
    return inner.split(',').map((x) => parseScalar(x.trim()));
  }
  // JSON 对象（内嵌的 JSON.stringify 出来的）
  if (t.startsWith('{') || t.startsWith('[')) {
    try { return JSON.parse(t); } catch { /* fall through */ }
  }
  // 数字 / 布尔
  if (t === 'true') return true;
  if (t === 'false') return false;
  if (t === 'null') return null;
  if (/^-?\d+$/.test(t)) return Number(t);
  if (/^-?\d+\.\d+$/.test(t)) return Number(t);
  return t;
}

export function renderFrontmatter(data: WikiFrontmatter): string {
  const lines: string[] = ['---'];
  for (const [k, v] of Object.entries(data)) {
    if (v === undefined || v === null) continue;
    if (Array.isArray(v)) {
      if (v.length === 0) continue;
      // 对象数组（如 blocks[]）用 JSON 行内格式，每条一行
      const isObjectArray = v.every((x) => x && typeof x === 'object' && !Array.isArray(x));
      if (isObjectArray) {
        lines.push(`${k}:`);
        for (const item of v) {
          lines.push(`  - ${escapeYaml(JSON.stringify(item))}`);
        }
      } else {
        lines.push(`${k}:`);
        for (const item of v) lines.push(`  - ${escapeYaml(String(item))}`);
      }
    } else if (typeof v === 'object') {
      lines.push(`${k}: ${escapeYaml(JSON.stringify(v))}`);
    } else {
      lines.push(`${k}: ${escapeYaml(String(v))}`);
    }
  }
  lines.push('---');
  return lines.join('\n');
}

function escapeYaml(s: string): string {
  if (/[:#\n"']/.test(s) || /^\s/.test(s) || /\s$/.test(s)) {
    return `"${s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
  }
  return s;
}

// ============================================================
// block 提取 / 替换
// ============================================================

const BLOCK_RE_FACTORY = (id: string) =>
  new RegExp(`<!--\\s*block:${escapeRegex(id)}\\s*-->[\\s\\S]*?<!--\\s*\\/block:${escapeRegex(id)}\\s*-->`);

const ANY_BLOCK_RE = /<!--\s*block:([^\s>]+)\s*-->[\s\S]*?<!--\s*\/block:\1\s*-->/g;

export interface ExtractedBlock {
  id: string;
  raw: string;            // 含 <!-- block:xxx --> 包装的完整块
  content: string;        // 去掉包装的内容
}

export function extractBlocks(body: string): ExtractedBlock[] {
  const out: ExtractedBlock[] = [];
  for (const m of body.matchAll(ANY_BLOCK_RE)) {
    const id = m[1];
    const raw = m[0];
    const content = raw
      .replace(new RegExp(`<!--\\s*block:${escapeRegex(id)}\\s*-->\\s*\\n?`), '')
      .replace(new RegExp(`\\n?<!--\\s*\\/block:${escapeRegex(id)}\\s*-->`), '');
    out.push({ id, raw, content });
  }
  return out;
}

export function wrapBlock(id: string, content: string): string {
  return `<!-- block:${id} -->\n${content}\n<!-- /block:${id} -->`;
}

export function hasBlock(body: string, id: string): boolean {
  return BLOCK_RE_FACTORY(id).test(body);
}

/** 替换或追加（文末）一个 block；返回新 body */
export function upsertBlock(body: string, id: string, newContent: string): string {
  const wrapped = wrapBlock(id, newContent);
  if (hasBlock(body, id)) {
    return body.replace(BLOCK_RE_FACTORY(id), wrapped);
  }
  return `${body.trimEnd()}\n\n${wrapped}\n`;
}

/** 移除单个 block */
export function removeBlock(body: string, id: string): string {
  return body.replace(BLOCK_RE_FACTORY(id), '').replace(/\n{3,}/g, '\n\n');
}

function escapeRegex(s: string): string {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

