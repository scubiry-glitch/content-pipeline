export interface TaxonomyFlatNode {
  code: string;            // E07.LLM
  name: string;            // 大模型
  level: 1 | 2;
  parentCode?: string;     // E07
  parentName?: string;     // 人工智能
}

let _flatCache: TaxonomyFlatNode[] | null = null;

export function flattenTaxonomy(): TaxonomyFlatNode[] {
  if (_flatCache) return _flatCache;
  const out: TaxonomyFlatNode[] = [];
  for (const l1 of TAXONOMY) {
    out.push({ code: l1.code, name: l1.name, level: 1 });
    for (const child of l1.children ?? []) {
      out.push({
        code: child.code,
        name: child.name,
        level: 2,
        parentCode: l1.code,
        parentName: l1.name,
      });
    }
  }
  _flatCache = out;
  return out;
}

export function getTaxonomyNode(code: string): TaxonomyFlatNode | undefined {
  return flattenTaxonomy().find((n) => n.code === code);
}

export function isValidTaxonomyCode(code: string): boolean {
  return flattenTaxonomy().some((n) => n.code === code);
}

/** 给 prompt 用：列出所有 L2 code（不含 L1）以紧凑文本形式 */
export function renderTaxonomyForPrompt(): string {
  const lines: string[] = [];
  for (const l1 of TAXONOMY) {
    if (!l1.children || l1.children.length === 0) continue;
    const childList = l1.children.map((c) => `${c.code} ${c.name}`).join(' / ');
    lines.push(`  ${l1.code}-${l1.name}: ${childList}`);
  }
  // 加一个 E99.OTHER 兜底（如果不在 TAXONOMY 里）
  if (!isValidTaxonomyCode('E99.OTHER')) {
    lines.push('  E99-其他: E99.OTHER 其他');
  }
  return lines.join('\n');
}

/** 给定 free-text domain 字符串（如 "AI" / "金融科技"），尝试映射到 taxonomy_code */
export function mapFreeTextToTaxonomyCode(freeText: string): string | null {
  if (!freeText) return null;
  const norm = freeText.trim().toLowerCase();
  for (const node of flattenTaxonomy()) {
    if (node.name.toLowerCase() === norm) return node.code;
  }
  // 二分匹配（部分包含）
  for (const node of flattenTaxonomy()) {
    if (node.name.toLowerCase().includes(norm) || norm.includes(node.name.toLowerCase())) {
      return node.code;
    }
  }
  return null;
}
